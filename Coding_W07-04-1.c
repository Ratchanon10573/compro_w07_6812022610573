#include <stdio.h>

int main() {
    int level;
    // รับค่าจากผู้ใช้
    printf("Enter membership level (1-4): ");
    scanf("%d", &level);
    // ตรวจสอบระดับสมาชิกและแสดงผล
    if (level == 1) {
        printf("Silver Member: 5%% discount\n"); }
    else if (level == 2) {
        printf("Gold Member: 10%% discount + Reward points\n"); }
    else if (level == 3) {
        printf("Platinum Member: 15%% discount + Reward points + Birthday gift\n"); }
    else if (level == 4) {
        printf("Diamond Member: All benefits + VIP events\n");}
    else {
        printf("Invalid membership level\n");}

    return 0;
}
/* อภิปราย : เปรียบเทียบการใช้ if-else if กับ switch-case
1. ความยืดหยุ่น:
   - if-else if: มีความยืดหยุ่นสูงกว่า สามารถใช้กับเงื่อนไขที่ซับซ้อนและหลากหลายได้ เช่น การเปรียบเทียบช่วงของค่า หรือการใช้ตัวดำเนินการตรรกะ (&&, ||)
   - switch-case: เหมาะสำหรับการตรวจสอบค่าคงที่ (constant values) เท่านั้น ไม่สามารถใช้กับเงื่อนไขที่ซับซ้อนได้
2. ความชัดเจน:
    - if-else if: อาจทำให้โค้ดดูซับซ้อนและยากต่อการอ่านเมื่อมีหลายเงื่อนไข
    - switch-case: ทำให้โค้ดดูเรียบง่ายและชัดเจนเมื่อมีการตรวจสอบค่าคงที่หลายค่า
3. ประสิทธิภาพ:
    - if-else if: อาจมีประสิทธิภาพต่ำกว่าในกรณีที่มีหลายเงื่อนไข เนื่องจากต้องตรวจสอบเงื่อนไขทีละอัน
    - switch-case: มักมีประสิทธิภาพดีกว่าเมื่อมีหลายกรณีที่ต้องตรวจสอบ เนื่องจากคอมไพเลอร์สามารถเพิ่มประสิทธิภาพการทำงานได้
4. การใช้งาน:
    - if-else if: เหมาะสำหรับเงื่อนไขที่ซับซ้อนหรือมีการเปรียบเทียบที่หลากหลาย
    - switch-case: เหมาะสำหรับการตรวจสอบค่าคงที่ที่มีจำนวนจำกัดและชัดเจน
สรุป: การเลือกใช้ if-else if หรือ switch-case ขึ้นอยู่กับลักษณะของเงื่อนไขที่ต้องการตรวจสอบ ความซับซ้อนของโค้ด และความต้องการด้านประสิทธิภาพ
*/